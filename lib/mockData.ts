// ============================================
// HELPER FUNCTIONS
// ============================================

export function getProductById(id: string): Product | undefined {
  return mockProducts.find(p => p.id === id);
}

export function getWarehouseById(id: string): Warehouse | undefined {
  return mockWarehouses.find(w => w.id === id);
}

export function getSupplierById(id: string): Supplier | undefined {
  return mockSuppliers.find(s => s.id === id);
}

export function getInventoryItemsByProduct(productId: string): InventoryItem[] {
  return mockInventoryItems.filter(item => item.productId === productId);
}

export function getInventoryItemsByWarehouse(warehouseId: string): InventoryItem[] {
  return mockInventoryItems.filter(item => item.warehouseId === warehouseId);
}

export function getTotalQuantity(productId: string): number {
  return mockInventoryItems
    .filter(item => item.productId === productId)
    .reduce((sum, item) => sum + item.quantityAvailable, 0);
}

export function getLowStockProducts(): Product[] {
  return mockProducts.filter(product => {
    const totalQty = getTotalQuantity(product.id);
    return totalQty <= product.reorderPoint;
  });
}

export function getOutOfStockProducts(): Product[] {
  return mockProducts.filter(product => {
    const totalQty = getTotalQuantity(product.id);
    return totalQty === 0;
  });
}
